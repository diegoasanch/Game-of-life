export const createBoard = (rows: number, cols: number, random: boolean, heart=false): boardData  => {
    const array = []
    let row;
    let is_alive

    for (let i = 0; i < rows; i++) {
        row = []
        for (let j = 0; j < cols; j++) {
            if (random)
                is_alive = !!(Math.round(Math.random()))
            else if (heart)
                is_alive = !!(heartPattern[i][j])
            else
                is_alive = false;


            row.push({
                alive: is_alive,
                age: 0,
                row: i,
                column: j
            })
        }
        array.push(row)
    }
    return array
}
